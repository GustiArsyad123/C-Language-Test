// Matematika C++
// C++ memiliki banyak fungsi yang memungkinkan Anda untuk melakukan tugas matematika pada angka.
#include <iostream>
#include <cmath> // Include the cmath library
using namespace std;
int main()
{
    cout << "Nilai Min: " << min(3, 10) << endl;
    cout << "Nilai Max: " << max(10, 15) << endl;
    cout << "Nilai Sqrt: " << sqrt(64) << endl;
    cout << "Nilai Round: " << round(2.6) << endl;
    cout << "Nilai Log: " << log(2) << endl;
}

